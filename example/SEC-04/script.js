// BOOELAN LOGIC
// AND && => true if ALL are true
// OR || => true if ONE is true
// NOT ! => inverts true/ false value

var age = 25;

if (age < 20) {
  console.log("John is a teenager");
} else if (age > 20 && age < 30) {
  console.log("John is a young man");
} else {
console.log("John is a man");
}
